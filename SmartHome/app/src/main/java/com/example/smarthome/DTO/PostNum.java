package com.example.smarthome.DTO;

public class PostNum {
    private int board;
    private int notice;
    private int qna;

    public int getBoard() {
        return board;
    }

    public void setBoard(int board) {
        this.board = board;
    }

    public int getNotice() {
        return notice;
    }

    public void setNotice(int notice) {
        this.notice = notice;
    }

    public int getQna() {
        return qna;
    }

    public void setQna(int qna) {
        this.qna = qna;
    }
}
